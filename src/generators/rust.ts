// ─────────────────────────────────────────────────────────────────────────────
// Counter polling snippet (when no MMIO but counter present)
// ─────────────────────────────────────────────────────────────────────────────

function generateCounterPolling(props: MmioRegisterProperties | undefined): string[] {
  if (!props) return [];
  const countReg = props.registers.find(r => r.name === 'COUNT');
  if (!countReg) return [];

  return [
    `// ── Polling the counter from firmware ────────────────────────────────────`,
    `//`,
    `// The counter increments in hardware on each clock edge.`,
    `// Firmware reads the current count through the memory-mapped COUNT register.`,
    ``,
    `fn read_hardware_count(periph: &${toPascalCase(props.moduleName)}) -> u32 {`,
    `    // volatile_read ensures the compiler doesn't cache this value`,
    `    periph.count()`,
    `}`,
    ``,
  ];
}

// ─────────────────────────────────────────────────────────────────────────────
// Pure combinational circuit (no MMIO)
// ─────────────────────────────────────────────────────────────────────────────

function generatePureLogicComment(circuit: Circuit): string {
  const hasGates = circuit.nodes.some(n => ['not', 'and', 'or', 'xor'].includes(n.type));
  const lines = [
    `// Generated by Verilog + Rust Hardware Lab`,
    `//`,
    `// This circuit contains only combinational logic with no memory-mapped registers.`,
    `// Rust firmware typically does not interact with pure combinational logic directly.`,
    `//`,
  ];

  if (hasGates) {
    lines.push(`// To expose this logic to firmware, add a Memory-Mapped Register Block`);
    lines.push(`// from the toolbox and connect the output to its data_in port.`);
    lines.push(`//`);
    lines.push(`// The MMIO block creates STATUS and DATA registers that firmware can read.`);
    lines.push(``);
    lines.push(`// Example: if the combinational output feeds an MMIO block at 0x4000_0000:`);
    lines.push(``);
    lines.push(`// const PERIPH_BASE: usize = 0x4000_0000;`);
    lines.push(`//`);
    lines.push(`// fn read_output() -> u32 {`);
    lines.push(`//     let periph = unsafe { MyPeriph::new(PERIPH_BASE) };`);
    lines.push(`//     periph.data() // volatile read from hardware`);
    lines.push(`// }`);
  }

  return lines.join('\n');
}

