// ─────────────────────────────────────────────────────────────────────────────
// Rust firmware skeleton generator.
// Detects MMIO register blocks in the circuit and generates:
//   - #[repr(C)] register struct
//   - Driver struct with volatile read/write methods
//   - Optional firmbuf-rs integration snippet
// ─────────────────────────────────────────────────────────────────────────────

import type { Circuit, MmioRegisterProperties, TimerPwmProperties, SpiControllerProperties, PidControllerProperties, AdcProperties } from '../simulator/types';

export function generateRust(circuit: Circuit): string {
  const mmios = circuit.nodes.filter(n => n.type === 'mmio_register');
  const timers = circuit.nodes.filter(n => n.type === 'timer_pwm_capture');
  const spis = circuit.nodes.filter(n => n.type === 'spi_controller');
  const pids = circuit.nodes.filter(n => n.type === 'pid_controller');
  const adcs = circuit.nodes.filter(n => n.type === 'adc');
  const hasCounter = circuit.nodes.some(n => n.type === 'counter' || n.type === 'counter8');
  const hasIrq = circuit.nodes.some(n => n.type === 'interrupt_output');
  const hasFirmbuf = mmios.some(n => {
    const p = n.properties as MmioRegisterProperties;
    return p.moduleName.includes('firmbuf') || p.moduleName.includes('uart') || p.registers.some(r => r.name === 'DATA');
  });

  if (mmios.length === 0 && timers.length === 0 && spis.length === 0 && pids.length === 0 && adcs.length === 0) {
    return generatePureLogicComment(circuit);
  }

  const sections: string[] = [];

  sections.push(`// Generated by Verilog + Rust Hardware Lab`);
  sections.push(`// This code demonstrates firmware interaction with the simulated hardware.`);
  sections.push(`// It is educational, not a complete compilable driver.`);
  sections.push(`//`);
  sections.push(`// Key insight: Rust uses volatile reads/writes because the HARDWARE`);
  sections.push(`// can change register values at any time, outside normal program flow.`);
  sections.push(`// Without volatile, the compiler might cache the register value in a`);
  sections.push(`// CPU register and never see the hardware-updated value.`);
  sections.push(``);
  sections.push(`#![allow(dead_code)]`);
  sections.push(`use core::ptr::{read_volatile, write_volatile};`);
  sections.push(``);

  for (const mmioNode of mmios) {
    const props = mmioNode.properties as MmioRegisterProperties;
    sections.push(...generateMmioDriver(props, hasIrq));
  }

  for (const timerNode of timers) {
    const props = timerNode.properties as TimerPwmProperties;
    sections.push(...generateTimerDriver(props));
  }

  for (const spiNode of spis) {
    const props = spiNode.properties as SpiControllerProperties;
    sections.push(...generateSpiDriver(props));
  }

  for (const pidNode of pids) {
    const props = pidNode.properties as PidControllerProperties;
    sections.push(...generatePidDriver(props));
  }

  for (const adcNode of adcs) {
    const props = adcNode.properties as AdcProperties;
    sections.push(...generateAdcDriver(props));
  }

  if (hasFirmbuf) {
    sections.push(...generateFirmbufIntegration());
  }

  if (hasCounter) {
    sections.push(...generateCounterPolling(mmios[0]?.properties as MmioRegisterProperties | undefined));
  }

  return sections.join('\n');
}

// ─────────────────────────────────────────────────────────────────────────────
// MMIO driver generation
// ─────────────────────────────────────────────────────────────────────────────

function generateMmioDriver(props: MmioRegisterProperties, hasIrq: boolean): string[] {
  const structName = toPascalCase(props.moduleName);
  const regsStructName = `${structName}Regs`;
  const lines: string[] = [];

  // Register struct
  lines.push(`// ── Memory-mapped register layout for ${props.moduleName} ────────────────`);
  lines.push(`// Base address: ${props.baseAddress}`);
  lines.push(`// This struct maps directly onto the hardware register block.`);
  lines.push(`// #[repr(C)] ensures Rust lays out fields at the exact byte offsets.`);
  lines.push(`#[repr(C)]`);
  lines.push(`pub struct ${regsStructName} {`);
  for (const reg of props.registers) {
    const rustType = reg.width === 8 ? 'u8' : reg.width === 16 ? 'u16' : 'u32';
    lines.push(`    /// ${reg.description}  [${reg.access}]  offset: +${reg.offset}`);
    lines.push(`    pub ${reg.name.toLowerCase()}: ${rustType},`);
  }
  lines.push(`}`);
  lines.push(``);

  // Driver struct
  lines.push(`pub struct ${structName} {`);
  lines.push(`    regs: *mut ${regsStructName},`);
  lines.push(`}`);
  lines.push(``);

  // impl block
  lines.push(`impl ${structName} {`);
  lines.push(`    /// Create driver from base address.`);
  lines.push(`    /// # Safety`);
  lines.push(`    /// Caller must ensure \`base\` is the correct peripheral base address.`);
  lines.push(`    pub const unsafe fn new(base: usize) -> Self {`);
  lines.push(`        Self { regs: base as *mut ${regsStructName} }`);
  lines.push(`    }`);
  lines.push(``);

  for (const reg of props.registers) {
    const rustType = reg.width === 8 ? 'u8' : reg.width === 16 ? 'u16' : 'u32';
    const fieldName = reg.name.toLowerCase();

    if (reg.access !== 'wo') {
      lines.push(`    /// Read ${reg.name} register (volatile – hardware may change this).`);
      lines.push(`    pub fn ${fieldName}(&self) -> ${rustType} {`);
      lines.push(`        unsafe { read_volatile(&(*self.regs).${fieldName}) }`);
      lines.push(`    }`);
      lines.push(``);
    }

    if (reg.access !== 'ro') {
      lines.push(`    /// Write ${reg.name} register.`);
      lines.push(`    pub fn set_${fieldName}(&self, val: ${rustType}) {`);
      lines.push(`        unsafe { write_volatile(&mut (*self.regs).${fieldName}, val) }`);
      lines.push(`    }`);
      lines.push(``);
    }
  }

  // Convenience methods
  const statusReg = props.registers.find(r => r.name === 'STATUS');
  const dataReg = props.registers.find(r => r.name === 'DATA' || r.name === 'DATA_RX');
  const irqClrReg = props.registers.find(r => r.name === 'IRQ_CLR');

  if (statusReg) {
    lines.push(`    /// Returns true when hardware has new data ready (STATUS bit 0).`);
    lines.push(`    pub fn rx_ready(&self) -> bool {`);
    lines.push(`        self.status() & 0x1 != 0`);
    lines.push(`    }`);
    lines.push(``);
    lines.push(`    /// Returns true if an overrun occurred (STATUS bit 1).`);
    lines.push(`    /// Overrun means firmware was too slow – a byte was lost.`);
    lines.push(`    pub fn overrun(&self) -> bool {`);
    lines.push(`        self.status() & 0x2 != 0`);
    lines.push(`    }`);
    lines.push(``);
  }

  if (dataReg) {
    lines.push(`    /// Read the received byte. Always use rx_ready() first!`);
    lines.push(`    pub fn read_data(&self) -> u8 {`);
    lines.push(`        self.data() as u8`);
    lines.push(`    }`);
    lines.push(``);
  }

  if (hasIrq && irqClrReg) {
    lines.push(`    /// Clear the interrupt by writing to IRQ_CLR.`);
    lines.push(`    /// Call this at the end of the interrupt handler.`);
    lines.push(`    pub fn clear_irq(&self) {`);
    lines.push(`        self.set_irq_clr(1);`);
    lines.push(`    }`);
    lines.push(``);
  }

  lines.push(`}`);
  lines.push(``);

  return lines;
}

